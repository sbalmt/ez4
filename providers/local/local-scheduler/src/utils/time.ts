const ONE_SECOND = 1000;
const ONE_MINUTE = 60 * ONE_SECOND;
const ONE_HOUR = 60 * ONE_MINUTE;
const ONE_DAY = 24 * ONE_HOUR;

export const TimeUnits = {
  second: ONE_SECOND,
  seconds: ONE_SECOND,
  minute: ONE_MINUTE,
  minutes: ONE_MINUTE,
  hour: ONE_HOUR,
  hours: ONE_HOUR,
  day: ONE_DAY,
  days: ONE_DAY
};
